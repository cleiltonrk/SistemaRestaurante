    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        String cod = iptPedido.getText();

        Connection con = ConexaoDAO.AbrirConexao();

        PedidoDAO sql = new PedidoDAO(con);

        Pedido a = new Pedido();

        if (cod.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum Nome Selecionado!", "Sistema de Restaurante", JOptionPane.WARNING_MESSAGE);
        } else {
            int b = JOptionPane.showConfirmDialog(null, "Deseja Realmente Excluir o Pedido: " + "\n ( " + cod + " ? )", "Sistema de Restaurante", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (b == 0) {
                int codigo = Integer.parseInt(cod);
                a.setId_pedido(codigo);
                String res = sql.ExcluirPedido(a);
                System.out.println(res);
                ConexaoDAO.FecharConexao(con);
                JOptionPane.showMessageDialog(null, "Pedido exclu√≠da com sucesso!", "Sistema Restaurante", JOptionPane.WARNING_MESSAGE);

                MesaDAO bd = new MesaDAO(con);
                String codM = mesaId.getText();
                System.out.println(codM);

                int convertido = Integer.parseInt(codM);

                RegistroMesas c = new RegistroMesas();
                c.InserirDados(convertido);
                c.AtualizaTablePedido(convertido);
                c.setVisible(true);
                dispose();
            }
        }
    }//GEN-LAST:event_jPanel3MouseClicked

