    private void jPanel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel10MouseClicked

        String nome = iptNome2.getText();
        String cep = iptCEP2.getText();
        String rua = iptRua2.getText();
        String numero = iptNumero2.getText();
        String bairro = iptBairro2.getText();
        String Grau_escolaridade = (String) iptGrau_escolaridade1.getSelectedItem();
        String fone = iptTelefone2.getText();
        String cpf = iptCPF2.getText();
        String Data_nasc = iptData_nasc1.getText();
        String login = iptLogin2.getText();
        String senha = iptSenha2.getText();
        String id = iptId2.getText();
        int idN = Integer.parseInt(id);

        if (nome.equals("") || cep.equals("") || rua.equals("") || numero.equals("") || bairro.equals("") || fone.equals("") || cpf.equals("") || Grau_escolaridade.equals("") || login.equals("") || senha.equals("") || Data_nasc.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum Campo pode Estar Vazio!", "Sistema Restaurante", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = ConexaoDAO.AbrirConexao();
            GarcomDAO sql = new GarcomDAO(con);
            int n = Integer.parseInt(numero);

            Garcom a = new Garcom();

            a.setBairro(bairro);
            a.setCep(cep);
            a.setCpf(cpf);
            a.setGrau_escolaridade(Grau_escolaridade);
            a.setNome(nome);
            a.setNumero(n);
            a.setData_nasc(Data_nasc);
            a.setRua(rua);
            a.setTelefone(fone);
            a.setLogin(login);
            a.setSenha(senha);
            a.setId(idN);

            String resultado = sql.AlterarGarcom(a);

            System.out.println(resultado);
            ConexaoDAO.FecharConexao(con);

            iptNome2.setText("");
            iptCEP2.setText("");
            iptNumero2.setText("");
            iptBairro2.setText("");
            iptGrau_escolaridade1.setSelectedItem("");
            iptTelefone2.setText("");
            iptRua2.setText("");
            iptData_nasc1.setText("");
            iptCPF2.setText("");
            iptLogin2.setText("");
            iptSenha2.setText("");
            iptId2.setText("");

            JOptionPane.showMessageDialog(null, "Alteração Realizada com Sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);

            

        }
    }//GEN-LAST:event_jPanel10MouseClicked

