    private void jPanel11jPanel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel11jPanel5MouseClicked
        String codigo = attCod2.getText();
        Connection con = ConexaoDAO.AbrirConexao();
        GarcomDAO sql = new GarcomDAO(con);

        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um Código para Atualizar", "Sistema Restaurante", JOptionPane.WARNING_MESSAGE);
        } else {
            int cod = Integer.parseInt(codigo);
            if (sql.TestarGarcom(cod) == false) {
                JOptionPane.showMessageDialog(null, "Código não encontrado no Banco", "Sistema Restaurante", JOptionPane.ERROR_MESSAGE);
                ConexaoDAO.FecharConexao(con);
            }

            iptNome2.setText("");
            iptBairro2.setText("");
            iptCEP2.setText("");
            iptCPF2.setText("");
            iptRua2.setText("");
            iptNumero2.setText("");
            iptGrau_escolaridade1.setSelectedItem("Selecione...");
            iptTelefone2.setText("");
            iptLogin2.setText("");
            iptSenha2.setText("");
            iptId2.setText("");
            iptData_nasc1.setText("");

            InserirDados(cod);
            attCod2.setText("");
        }
    }//GEN-LAST:event_jPanel11jPanel5MouseClicked

