    private void InserirDados(int cod) {
        Connection con = ConexaoDAO.AbrirConexao();
        GarcomDAO sql = new GarcomDAO(con);
        List<Garcom> lista = new ArrayList<>();
        lista = sql.CapturarGarcom(cod);

        for (Garcom a : lista) {
            iptId2.setText("" + a.getId());
            iptNome2.setText(a.getNome());
            iptData_nasc1.setText(a.getData_nasc());
            iptTelefone2.setText(a.getTelefone());
            iptRua2.setText(a.getRua());
            iptBairro2.setText(a.getBairro());
            iptCEP2.setText(a.getCep());
            iptNumero2.setText("" + a.getNumero());
            iptGrau_escolaridade1.setSelectedItem(a.getGrau_escolaridade());
            iptCPF2.setText(a.getCpf()); 
            iptLogin2.setText(a.getLogin());
            iptSenha2.setText(a.getSenha());
             
           

        }
    }

