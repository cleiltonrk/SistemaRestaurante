    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
          String nomeProd = txtNome_produto.getText();
         String preco = txtPreco.getText();
         String id_cat = Id_Cat.getText();
         int idCat = Integer.parseInt(id_cat);
        
         if(iptCat.getSelectedItem().toString() == "Selecione.."){
            JOptionPane.showMessageDialog(null, "Por favor, selecione o tipo de Categoria","Sistema Restaurante", JOptionPane.ERROR_MESSAGE);
        }if(nomeProd.equals("") || preco.equals("")){
             JOptionPane.showMessageDialog(null, "Nenhum Campo pode estar Vazio","Sistema Restaurante", JOptionPane.WARNING_MESSAGE);
        }else{
             Connection con = ConexaoDAO.AbrirConexao();
             ProdutoDAO sql = new ProdutoDAO(con);
             Produto a  = new Produto();
           
             a.setNome(nomeProd);
             a.setPreco(preco);
             a.setCategoria(idCat);
             
             
             String resultado =  sql.inserir_Produto(a);
              
              ConexaoDAO.FecharConexao(con);

            txtPreco.setText("");
            txtPreco.setText("");
            iptCat.setSelectedItem("");
            
             JOptionPane.showMessageDialog(null,"Produto Cadastrado com Sucesso!","Sistema de Restaurante",JOptionPane.INFORMATION_MESSAGE);
             
            dispose();
              
        }
    }//GEN-LAST:event_jPanel4MouseClicked

